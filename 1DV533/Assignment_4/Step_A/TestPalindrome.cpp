#include <iostream>
#include <cctype>

using namespace std;

int main()
{
    double score;
    int q1 = 7;
    int q2 = 8;
    int mE = 70;
    int fE = 80;

    score = ((q1 * 0.25 + q2 * 0.25) * 5)  + mE * 0.25 + fE * 0.5;

    cout << "The average score is: " << score;
    return 0;
}

